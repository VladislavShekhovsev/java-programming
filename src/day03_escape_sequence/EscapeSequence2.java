package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"Iphone\",\"Xbox\",\"Webcam\",\"Monitor\"");
        System.out.println("\"Iphone\"\t\"Webcam\"\t\"PS%\"");
        System.out.println("\"Iphone\", \"Webcam\" \n \"Monitor\",\t\"PS5\"");
        System.out.println("   ^   ");
        System.out.println(" /   \\");
        System.out.println(" |   |");
        System.out.println("|||||||");
    }
}
