package day14_multi_branch_if_statements;

public class MultiBranchIfStatement {
    public static void main(String[] args) {
        byte day = 2;
        if (day == 1) {
            System.out.println("its Sunday");
        } else if (day == 2) {
            System.out.println("its Monday");
        } else if (day == 3) {
            System.out.println("its tuesday");
        }


    }
}
