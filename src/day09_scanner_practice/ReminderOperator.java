package day09_scanner_practice;

public class ReminderOperator {
    public static void main(String[] args) {
        System.out.println(10/3);// 3
        System.out.println(10%3);// 3+3+3+1 (3 times 3 in ten and 1 remainder, that 1 is remainder
        System.out.println(10/5);// 10:5=2
        System.out.println(10%5); // zero (two fives in ten and no remainder.
        System.out.println(20/7); // 2 times in 20,20 cannot be combined more then 2 times
        // 7 (3 times its 21 its already above 20)
        System.out.println(20%7);//  6 remainder
    }
}
